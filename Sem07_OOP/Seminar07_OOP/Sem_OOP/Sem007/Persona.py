class Persona():
    Name: str
    Phone: str
    City: str

    def _init_(self, Name: str, Phone: str, City: str):
        self.Name = Name
        self.Phone = Phone
        self.City = City
    
    def Persona(self, Name: str, Phone: str, City: str):
        self.Name, self.Phone, self.City

    def getPhone(self):
        self.Phone
    
    def getName(self):
        self.Name

    def getCity(self):
        self.City
